package Ders_17;

import Ders_16.Lokal;

public class Tools extends Lokal {


    public int toplam(int sayi1, int sayi2) {

        return sayi1 + sayi2;
    }


    public int cikarma(int sayi1, int sayi2) {

        return sayi1 - sayi2;
    }

    public int carpma( int sayi1, int sayi2) {

        return sayi1 * sayi2;
    }

    public int bolme( int sayi1, int sayi2) {

        return sayi1 / sayi2;
    }



}